print('\nЗадание №2\n')

array1 = [3, 1, 2]
array2 = [6, 5, 4]
array3 = [9, 8, 7]

total_summ = 0


def avg_array():
    total_summ = sum(array1 + array2 + array3)
    total_avg = total_summ / (len(array1) + len(array2) + len(array3))
    print("Общая сумма:", total_summ, "Среднее значение:", total_avg)


avg_array()
